"""Ground truth eval using LLM-generated insights.

Same 100 characters, same ground truth, but insights are generated by
LLM from detector signals + user text (not templates).
"""

import asyncio
import json
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

from scripts.literary_validation import (
    heuristic_emotion,
    heuristic_conflict,
    heuristic_humor,
    heuristic_fragility,
)
from scripts.literary_gt_eval import score_insight_against_gt
from sufficiency_scorer.detectors.eq import EQAdapter
from sufficiency_scorer.insight_generator import InsightGenerator
from sufficiency_scorer.models import DetectorResult, Dimension

DATA_PATH = Path(__file__).resolve().parent.parent / "data" / "literary_first_words.json"
GT_PATH = Path(__file__).resolve().parent.parent / "data" / "literary_ground_truth.json"


async def main():
    with open(DATA_PATH) as f:
        characters = json.load(f)
    with open(GT_PATH) as f:
        ground_truths = {gt["id"]: gt for gt in json.load(f)}

    eq = EQAdapter()
    generator = InsightGenerator()

    all_scores = []
    char_results = []
    errors = 0

    for char in characters:
        cid = char["id"]
        gt = ground_truths.get(cid)
        if not gt:
            continue

        text = char["text"]

        # Run detectors (EQ real, others heuristic)
        eq_result = await eq.detect(text)
        emotion_result = heuristic_emotion(text)
        conflict_result = heuristic_conflict(text)
        humor_result = heuristic_humor(text)
        fragility_result = heuristic_fragility(text)

        results = [emotion_result, eq_result, conflict_result, humor_result, fragility_result]
        seen = {r.dimension for r in results}
        for dim in Dimension:
            if dim not in seen:
                results.append(DetectorResult(dimension=dim))

        # Generate insights via LLM
        try:
            insights = generator.generate(results, user_text=text)
        except Exception as e:
            print(f"  ERROR #{cid} {char['character']}: {e}")
            errors += 1
            continue

        # Score against ground truth
        insight_scores = []
        for ins in insights:
            score, reason = score_insight_against_gt(ins.reframe, gt)
            insight_scores.append({
                "score": score,
                "reason": reason,
                "reframe": ins.reframe[:100],
                "signal": ins.signal[:50],
            })
            all_scores.append(score)

        avg_score = sum(s["score"] for s in insight_scores) / len(insight_scores) if insight_scores else 0
        char_results.append({
            "id": cid,
            "character": char["character"],
            "avg_score": avg_score,
            "insight_count": len(insight_scores),
            "scores": insight_scores,
        })

        # Progress
        status = "✓" if avg_score >= 50 else "○" if avg_score >= 30 else "·"
        print(f"  {status} #{cid:>3} {char['character']:<25} avg={avg_score:.0f} insights={len(insights)}")

    # === Report ===
    print("\n" + "=" * 90)
    print("GROUND TRUTH EVAL — LLM-GENERATED INSIGHTS")
    print("=" * 90)

    char_results.sort(key=lambda x: x["avg_score"], reverse=True)

    print(f"\n--- Overall ---")
    print(f"  Characters: {len(char_results)} (errors: {errors})")
    print(f"  Total insights: {len(all_scores)}")
    if all_scores:
        print(f"  Mean score: {sum(all_scores)/len(all_scores):.1f}/100")
        print(f"  Median: {sorted(all_scores)[len(all_scores)//2]}/100")

    deep = sum(1 for s in all_scores if s >= 80)
    partial = sum(1 for s in all_scores if 60 <= s < 80)
    surface = sum(1 for s in all_scores if 40 <= s < 60)
    generic = sum(1 for s in all_scores if 10 < s < 40)
    wrong = sum(1 for s in all_scores if s <= 10)
    print(f"\n--- Score Distribution ---")
    print(f"  80-100 (DEEP — 'how did you know?!'):  {deep:>3} ({deep/len(all_scores)*100:.0f}%)")
    print(f"  60-79  (PARTIAL DEEP):                  {partial:>3} ({partial/len(all_scores)*100:.0f}%)")
    print(f"  40-59  (SURFACE):                       {surface:>3} ({surface/len(all_scores)*100:.0f}%)")
    print(f"  11-39  (GENERIC):                       {generic:>3} ({generic/len(all_scores)*100:.0f}%)")
    print(f"   0-10  (WRONG):                         {wrong:>3} ({wrong/len(all_scores)*100:.0f}%)")

    print(f"\n--- Top 15 (Most Impressed) ---")
    for r in char_results[:15]:
        print(f"  {r['avg_score']:>5.0f}  #{r['id']:>3} {r['character']:<25} ({r['insight_count']} insights)")
        for s in r["scores"][:2]:
            print(f"         [{s['score']:>3}] {s['reason'][:50]}")
            print(f"               → {s['reframe'][:75]}")

    print(f"\n--- Bottom 15 ---")
    for r in char_results[-15:]:
        print(f"  {r['avg_score']:>5.0f}  #{r['id']:>3} {r['character']:<25} ({r['insight_count']} insights)")
        for s in r["scores"][:2]:
            print(f"         [{s['score']:>3}] {s['reason'][:50]}")
            print(f"               → {s['reframe'][:75]}")

    # WRONG insights
    wrongs = []
    for r in char_results:
        for s in r["scores"]:
            if s["score"] <= 10:
                wrongs.append({"character": r["character"], **s})

    if wrongs:
        print(f"\n--- WRONG ({len(wrongs)}) ---")
        for w in wrongs[:15]:
            print(f"  {w['character']:<25} → {w['reframe'][:70]}")
            print(f"    {w['reason']}")

    output = Path(__file__).resolve().parent.parent / "data" / "literary_gt_scores_llm.json"
    with open(output, "w") as f:
        json.dump(char_results, f, indent=2, ensure_ascii=False)
    print(f"\nSaved to {output}")


if __name__ == "__main__":
    asyncio.run(main())
